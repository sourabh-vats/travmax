<p>We want to help you get your items as fast and affordably as possible. Below are the details of how we get your purchased product to your door. If you have any questions, please submit a customer service contact form here.</p>
<h4><b>1. Shipping Communication</b></h4>
<p>Wishzonwill keep you informed about your purchase by sending information to the email address you provided at checkout.  If you selected to receive text messages, we’ll also send this same information to the mobile number provided. Here’s what you’ll receive:</p>
<p><b>1.</b> a confirmation of your completed order</p>
<p><b>2.</b> if your order is going to take longer than 5 business days to ship, we will notify you with an estimated shipping date</p>
<p><b>3.</b> once it ships we send a final communication telling  you who the carrier is, and the tracking number</p>
<p><b>4.</b>for local deliveries, many of whom don’t use tracking numbers, we’ll provide you with the name and phone number of the carrier</p>

<h4><b>2. Free Shipping.</b></h4>

<p>We’re very happy to offer Free Shipping* to most destinations across the INDIA. Some restrictions apply.</p>
<p>For orders that are under Rs.99/. there is a Rs.50/. flat shipping charge that applies to every order shipped in the INDIA, except those listed below as exceptions.</p>

<h4><b>3. Free Shipping Exceptions</b></h4>

    <p>* Shipping addresses located in Hawaii and Alaska will be subject to a Rs.65 flat shipping charge on all orders. For heavy and oversized items, shipping charges to these locations will be assessed on an order-by-order basis. In exceptional circumstances additional shipping charges may apply. Speak to a customer service team member if you have any questions.</p>

<h4><b>4. Warehouses</b></h4>

<p>Wishzonships our products from a number of warehouses located across the INDIA to ensure timely delivery and excellent customer service..</p>

<h4><b>5. Delivery Time.</b></h4>

<p><b>1.</b> Most orders received by Wishzonwill be shipped the following business day. Most orders arrive within 5 business days but due to the combination of carriers, warehouse location and customer address, we cannot promise that items will arrive within that time frame.</p>
<p><b>2.</b> If an item is out of stock we will let you know as quickly as possible and will provide you with an ETA for the item or we will help you find an alternative product.</p>
<p><b>3.</b> If your order is time-sensitive, please contact us so we can try to arrange a timely delivery.</p>

<h4><b>6. Carriers</b></h4>

<p>With the breadth of products we sell, and the large geography we cover, Wishzonpartners with a number of freight carries in the INDIA. Most purchases are shipped by UPS Ground but for large and/or heavy shipments that cannot be shipped with these carriers, we will use a local less-than-truckload (LTL) carrier.</p>
<p>When a delivery address is within very close proximity to our shipping warehouse, we often use local parcel/freight carriers other than the carriers listed above.</p>


 <h4><b>7. Shipping Errors</b></h4> 
<p>If you believe you have received the wrong product, have been short-shipped, or believe any other product or quantity error has occurred, please report to Wishzonwithin 1 week of product delivery.  Shipping errors not reported within this time frame may not be corrected.</p>

<h4><b>8. Shipping Damage</b></h4>
<p>Despite our best packing efforts, some orders may be damaged in shipment. If you have received an order with damages you can do the following:</p>
<p><b>1.</b> Refuse shipment if the package is clearly damaged</p>
<p><b>2.</b> Immediately inspect the outer packaging and contents for shipping damage</p>
<p><b>3.</b> Report this damage to Wishzonwithin 24 hours of delivery</p>
<p>If you don’t report damages to us within 24 hours of product delivery it will be impossible for us to make a claim with the carrier and in turn will make it impossible for us to reimburse you for shipping damages.</p>
<p>Even if you do not intend to install the product right away, any damages must be reported to us within 24 hours of delivery.</p>
<p>If you have any questions or concerns about our shipping policies, please feel free to contact us.</p>
<h4><b>9. Multiple Package Shipments</b></h4>
<p>For Wishzonto deliver your order as quickly as possible we require that delivery details are accurate. We are not responsible for orders that are delayed, lost, or misdirected as a result of an inaccurate address being provided.</p>
<p>Wishzonwill not be responsible for additional shipping charges that result from inaccurate or incomplete address information.</p>
<p>If we discover an error in shipping information we will attempt to contact you to correct the problem but it is ultimately your responsibility to provide accurate information.</p>
<h4><b>10. Delivery to Remote Locations</b></h4>
<p>In the rare case that the delivery address is in a location where our usual shipping methods aren’t available, there will be an additional cost for delivery. In cases like this we will contact you before we ship your order to ensure that you are aware of the additional cost. The cost will varying depending on location.</p>
